              <h3>Транспорт</h3>
<br>&nbsp;&nbsp;&nbsp;&nbsp;
<br>&nbsp;&nbsp;&nbsp;&nbsp;В Швейцарии можно ездить с российскими правами международного образца (выдаются в вашем ГАИ в России, по предъявлении внутренних) всего один год. За это время нужно взять несколько уроков здесь, поскольку существуют некоторые особенности вождения и вообще, наличие взятых уроков производит хорошее впечатление. Кроме того, инструктор организовывает сдачу экзамена. Сдавать можно на своей машине (рекомендую). Если у вас автомат, никто не заставляет учить на ручник. От места к месту свои длительности и сложности сдачи практики (сдается только контрольная поездка). Уточню, вы имееете право с русскими правами ездить в течение одного года, пересдать на швейцарские права (только контрольную поездку) можете в течение трех лет, права не обязаны быть "книжечкой". Возможно, в других кантонах смотрят на это дело иначе, однако закон прописан именно так.
<br>&nbsp;&nbsp;&nbsp;&nbsp;Российские права вам вернут, независимо от результата контрольной поездки, на моих никаких штампов не ставили, прото в прилагавшемся письме написали, что права признаны недействительными территории Швейцарии, и ездить я сама не имею права, пока не докажу обратное, то есть мне пришлось в итоге все заново учить и сдавать. Все мои знакомые, получившие здешние права, тоже и не думали о международных. Если получать автомобильные права с нуля то процесс длительный и нудный. 
